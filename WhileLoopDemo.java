public class WhileLoopDemo {

    public static void main(String[] args) {

        int i = 1;

        while (i <= 5)

        {

            System.out.println("Hello, Welcome to the Java Course, Aliens!!" + "Value of i is: " + i);

            i++;

            System.out.println("Value of i after incrementation: " + i);

        }

        System.out.println("Bye!!!!");

    }

}
